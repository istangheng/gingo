package main

import (
	"gingo/initialize"
	"gingo/internal/config"
	"gingo/router"

	"github.com/gin-gonic/gin"
	_ "github.com/go-sql-driver/mysql"
)

func init() {
	config.InitConf()
	initialize.InitDB()
	initialize.InitRedisPool()
	initialize.InitCron()
	initialize.InitRbac()
}

